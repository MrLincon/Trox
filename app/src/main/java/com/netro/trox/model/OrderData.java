package com.netro.trox.model;

public class OrderData {

    String sender_name, sender_contact, sender_address, order_id, order_status;

    public OrderData() {
    }

    public String getSender_name() {
        return sender_name;
    }

    public void setSender_name(String sender_name) {
        this.sender_name = sender_name;
    }

    public String getSender_contact() {
        return sender_contact;
    }

    public void setSender_contact(String sender_contact) {
        this.sender_contact = sender_contact;
    }

    public String getSender_address() {
        return sender_address;
    }

    public void setSender_address(String sender_address) {
        this.sender_address = sender_address;
    }

    public String getOrder_id() {
        return order_id;
    }

    public void setOrder_id(String order_id) {
        this.order_id = order_id;
    }

    public String getOrder_status() {
        return order_status;
    }

    public void setOrder_status(String order_status) {
        this.order_status = order_status;
    }
}
